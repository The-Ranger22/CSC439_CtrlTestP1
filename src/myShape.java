/**
 *
 * The abstract class myShape is used to list the common methods of any class designed to
 * represent a shape.
 *
 * @author  Levi Schanding
 * @version 1.1
 */
public abstract class myShape {
    /**
     * @return when implemented by a child class, returns the area of the shape
     */
    public abstract double area();

    /**
     * @return when implemented by a child class, returns the perimeter of the shape
     */
    public abstract double perimeter();
}
